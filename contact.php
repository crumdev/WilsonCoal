<script type="text/javascript"> 
	$(document).ready(function() {
		//Checks the "Reason for contact" dropdown for a change and reveals more questions if 
		//interested in purchasing a product. Hides the 
		$('#reason').change(function(){
			if($(this).val() === 'purchase') {
				$('#product-info').slideDown('slow');
			} else {
				$('#product-info').slideUp();
				$('#product-info').find('input, select, radio, checkbox').each(function(){ 
					$(this).reset(); 
				});
			}
		});
			
		/* Disable submit until all require fields are filled.
		** 
		** Once any field loses focus it checks to see if the required fields are filled,
		** it will highlight the empty ones by adding a prestyled warning class to the 
		** surrounding paragraph "<p>" element.
		**
		** If any are empty it also ensures that the submit button remains disabled. Once
		** the required fields have input it will enable the button again.
		*/
		$('#contact-form').find('input, select, textarea').blur(function() { 
			var badEmail = false;
			var empty = false;
			$('#contact-form').find('input, select, textarea').filter('.required').each(function(){ 
				if($.trim($(this).val()) === '') {
					empty = true;
					$(this).closest('p').removeClass('warning');
					$(this).closest('p').addClass('warning');
				} else if($(this).hasClass('email')) {
					var emailReg = /^([\w-\.]+@([\w-]+\.)+[\w-]{2,4})?$/;
					if(!emailReg.test($.trim($(this).val()))) {
						$(this).siblings('span').remove();
						$(this).parent().append('<span class="warning">Sorry! You\'ve entered an invalid email.</span>');
						badEmail = true;
					} else {
						$(this).siblings('span').remove();
						$(this).closest('p').removeClass('warning');
					}
				} else {
					$(this).closest('p').removeClass('warning');
				}
			});
			
			if(empty || badEmail) {
				$('#submit').attr('disabled', 'disabled');
			} else {
				$('#submit').removeAttr('disabled');
			}
		});
		
		//Confirm form submission
		$('#contact-form').submit(function(event) {
			
			//Stop form from submitting normally
			event.preventDefault();
			
			$.post( '_scripts/mail.php', $("#contact-form").serialize(),
				function( data ) {
					$("#status").append(data);
					$('#form').slideUp();
					$('#contact-form').reset();
				}
			);
		});
		
		//Clear fields and warnings (will not work unless submit button is enabled).
		$('#reset').click(function(){
			$('#contact-form').find('.required').each(function() {
				$(this).closest('p').removeClass('warning');
			});
			$('#contact-form').find('input, select, radio').not('#submit, #reset').each(function(){ 
				$('#product-info').slideUp();
				$(this).reset(); 
			});
		});
	});
</script>
